<p>3.市场启动时，首次版本以解决方案发布的版本为准，市场提出问题或需求时，问题对接人需要获取版本信息文件，如未切分支需要分支切换之后进行问题跟进，问题处理过程中的测试版本无需更新版本信息，问题处理完成后提交git版本，最后打包正式版本发布给市场，并通知市场以正式版本为准。</p>
