<h2 id="背景" tabindex="-1"><a class="header-anchor" href="#背景"><span>背景</span></a></h2>
<p>该文档主要用于指导产品针对解决方案及市场的版本进行管理，规范产品版本发布流程及源码管理流程。</p>
<h2 id="目前面临问题" tabindex="-1"><a class="header-anchor" href="#目前面临问题"><span>目前面临问题</span></a></h2>
<p>1.市场版本与源码不一致问题，源码修改发布后无法与市场版本有效关联；
2.市场版本脱离产品管控范围，市场不使用申请的产品版本，产品版本拼凑后使用；
3.解决方案为保证发版后的快速部署，统一发布全量版本，产品缺少切分支及更新源码的契机；
4.前端组件库以全量方式打包，不支持实时调试，也不支持差量更新，修改市场组件时环境准备较复杂；
5.市场定制化较多，需要切市场分支进行维护。</p>
<h2 id="发版流程" tabindex="-1"><a class="header-anchor" href="#发版流程"><span>发版流程</span></a></h2>
